void main() {
  //Comentarios en Dart
  // Un comentario de una línea comienza con dos barras // y se extiende hasta el final de la línea.
  print('Hola, Dart!'); // Este es un comentario al final de una línea
  /*
   * Un comentario de varias líneas comienza con una barra y un asterisco /* y termina con un asterisco y una barra */.
   * Se puede usar para explicar bloques de código más largos o para desactivar temporalmente partes del código.
   */
  print('Este es un ejemplo de comentario de varias líneas.');

  /// Un comentario de documentación comienza con tres barras /// y se utiliza para documentar funciones, clases o bibliotecas.
  /// Este es un ejemplo de comentario de documentación.
  /// Puede ser utilizado por herramientas de documentación para generar documentación automáticamente.
  /// Por ejemplo, este comentario documenta la función main.
  main(); // Llamada a la función main
  // Los comentarios son útiles para explicar el propósito del código, aclarar la lógica o dejar notas para otros desarrolladores.
  // También se pueden usar para desactivar temporalmente partes del código durante el desarrollo.
}
