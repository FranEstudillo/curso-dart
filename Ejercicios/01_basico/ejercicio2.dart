void main() {
  // Tipos de datos numéricos
  // Enteros
  int entero = 42;
  print('Entero: $entero');

  // Doble
  double decimal = 3.14;
  print('Doble: $decimal');

  // Operaciones aritméticas
  int suma = entero + 10;
  double multiplicacion = decimal * 2.0;

  print('Suma: $suma');
  print('Multiplicación: $multiplicacion');

  // Conversión de tipos
  double convertido = entero.toDouble();
  print('Convertido a doble: $convertido');
}
